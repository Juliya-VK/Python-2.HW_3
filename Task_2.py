"""Задание 2.  В большой текстовой строке подсчитать количество встречаемых слов и вернуть 10 самых частых. 
Не учитывать знаки препинания и регистр символов. 
За основу возьмите любую статью из википедии или из документации к языку. """


text = """
Python — высокоуровневый язык программирования общего назначения с динамической строгой типизацией 
и автоматическим управлением памятью, ориентированный на повышение производительности разработчика, 
читаемости кода и его качества, а также на обеспечение переносимости написанных на нём программ. 
Язык является полностью объектно-ориентированным — всё является объектами. 
Необычной особенностью языка является выделение блоков кода пробельными отступами. 
Синтаксис ядра языка минималистичен, за счёт чего на практике редко возникает необходимость обращаться к документации. 
Python является мультипарадигменным языком программирования, поддерживающим императивное, процедурное, 
структурное, объектно-ориентированное программирование, метапрограммирование и функциональное программирование. 
Задачи обобщённого программирования решаются за счёт динамической типизации. 
Python часто используется как язык для написания скриптов, но его диапазон применения значительно шире.
"""

# 1. Приведем текст к нижнему регистру
lower_text = text.lower()

# 2. Удалим знаки препинания
no_punctuation = ""
for char in lower_text:
    if char.isalpha() or char == " " or char == "\n":
        no_punctuation += char

# 3. Разделим текст на слова
words = no_punctuation.split()

# 4. Создадим словарь для подсчета слов
word_count = {}
for word in words:
    if word in word_count:
        word_count[word] += 1
    else:
        word_count[word] = 1

# 5. Преобразуем словарь в список кортежей (слово, количество)
word_list = list(word_count.items())

# 6. Отсортируем список по убыванию количества
# Простая сортировка пузырьком
n = len(word_list)
for i in range(n):
    for j in range(0, n-i-1):
        if word_list[j][1] < word_list[j+1][1]:
            word_list[j], word_list[j+1] = word_list[j+1], word_list[j]

# 7. Выведем 10 самых частых слов
print("10 самых частых слов:")
for i in range(10):
    if i < len(word_list):
        print(f"{i+1}. {word_list[i][0]} - {word_list[i][1]}")
